/**
 * C program to find square root of a number
 */

#include <stdio.h>
#include <math.h>

int main()
{
    float num=144, root;

    /* Input a number from user */
   // ("Enter any number to find square root: ");
//("%f", &num);

    /* Calculate square root of num */
    root = sqrt(num);

    /* Print the resultant value */
    ("Square root of %.2f = %.2f", num, root);

    return 0;
}

 
